package com.itwill.billycar.vo;

public class CouponVO {
	
	private String coupon_code;
	private String coupon_name;
	private int coupon_discount_amount;
	private int coupon_limit_number;
	
	public String getCoupon_code() {
		return coupon_code;
	}
	public void setCoupon_code(String coupon_code) {
		this.coupon_code = coupon_code;
	}
	public String getCoupon_name() {
		return coupon_name;
	}
	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}
	public int getCoupon_discount_amount() {
		return coupon_discount_amount;
	}
	public void setCoupon_discount_amount(int coupon_discount_amount) {
		this.coupon_discount_amount = coupon_discount_amount;
	}
	public int getCoupon_limit_number() {
		return coupon_limit_number;
	}
	public void setCoupon_limit_number(int coupon_limit_number) {
		this.coupon_limit_number = coupon_limit_number;
	}
	
	

}
